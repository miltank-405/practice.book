	/* 
		--- なにをやってるのかイメージしながら書くことが大事 ---
		let  <--今から変数を使うおー
		let input  <--変数名はinputにするおー
		let input = 0;  <--inputに0入れるおー
		--- 書き方だけを覚えるんじゃなくて、書こうとしているコードの意味を頭の中で声出す心がけが大事 ---
	 */
